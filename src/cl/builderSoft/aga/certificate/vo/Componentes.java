package cl.builderSoft.aga.certificate.vo;

public class Componentes {
	private String componente = "";
	private String sigla = "";
	private String composicion = "";
	private String analisis = "";
	private String unidad = "";
	private String desviacionAbsoluta = "";
	private String desviacionRelativa = "";

	public String getSigla() {
		return sigla;
	}

	public void setSigla(String sigla) {
		this.sigla = sigla;
	}

	public String getComponente() {
		return componente;
	}

	public void setComponente(String componente) {
		this.componente = componente;
	}

	public String getComposicion() {
		return composicion;
	}

	public void setComposicion(String composicion) {
		this.composicion = composicion;
	}

	public String getAnalisis() {
		return analisis;
	}

	public void setAnalisis(String analisis) {
		this.analisis = analisis;
	}

	public String getUnidad() {
		return unidad;
	}

	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}

	public String getDesviacionAbsoluta() {
		return desviacionAbsoluta;
	}

	public void setDesviacionAbsoluta(String desviacionAbsoluta) {
		this.desviacionAbsoluta = desviacionAbsoluta;
	}

	public String getDesviacionRelativa() {
		return desviacionRelativa;
	}

	public void setDesviacionRelativa(String desviacionRelativa) {
		this.desviacionRelativa = desviacionRelativa;
	}

}
